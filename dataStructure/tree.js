/**
 * Tree 树
 * 高度：叶节点 -> 根节点，0开始
 * 深度：根节点 -> 叶节点，0开始
 * 层数：根节点 -> 叶节点，1开始
 */

/**
 * 二叉树
 * 
 * 完全二叉树：叶子节点都在最底下两层，最后一层叶子节都靠左排列，并且除了最后一层，其他层的节点个数都要达到最大
 */

 /**
 * Heap 堆 - 完全二叉树
 * 堆中每一个节点的值都必须大于等于（或小于等于）其子树中每个节点的值。
 * 大顶堆：每个节点的值都大于等于子树中每个节点值的堆
 * 小顶堆：每个节点的值都小于等于子树中每个节点值的堆
 */